public class BookAnAppointmentForm
{

	private String modelNumber;
	private String typeOfService;
	private String time;
	private String customerDetails;
	private String orderNo;

	/**
	 * @return the modelNumber
	 */
	public String getModelNumber()
	{
		return modelNumber;
	}

	/**
	 * @param modelNumber
	 *           the modelNumber to set
	 */
	public void setModelNumber(final String modelNumber)
	{
		this.modelNumber = modelNumber;
	}

	/**
	 * @return the typeOfService
	 */
	public String getTypeOfService()
	{
		return typeOfService;
	}

	/**
	 * @param typeOfService
	 *           the typeOfService to set
	 */
	public void setTypeOfService(final String typeOfService)
	{
		this.typeOfService = typeOfService;
	}

	/**
	 * @return the time
	 */
	public String getTime()
	{
		return time;
	}

	/**
	 * @param time
	 *           the time to set
	 */
	public void setTime(final String time)
	{
		this.time = time;
	}

	/**
	 * @return the customerDetails
	 */
	public String getCustomerDetails()
	{
		return customerDetails;
	}

	/**
	 * @param customerDetails
	 *           the customerDetails to set
	 */
	public void setCustomerDetails(final String customerDetails)
	{
		this.customerDetails = customerDetails;
	}

	/**
	 * @return the orderNo
	 */
	public String getOrderNo()
	{
		return orderNo;
	}

	/**
	 * @param orderNo
	 *           the orderNo to set
	 */
	public void setOrderNo(final String orderNo)
	{
		this.orderNo = orderNo;
	}

}
